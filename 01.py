# see impordib kilpkonna mooduli
import turtle

turtle.speed(0) # reguleeri 1-9
turtle.penup()
turtle.goto(-500,200)
turtle.pendown()
turtle.forward(200) #fd, pikslites
turtle.left(120) #lt, rt
turtle.forward(200) #fd, pikslites
turtle.left(120) #lt, rt
turtle.forward(200) #fd, pikslites


# lõpetab kilpkonna, et ei jookseks kokku 
turtle.done()

